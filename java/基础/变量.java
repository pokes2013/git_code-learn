//变量的申明及调用

public class 变量 {
    public static void main(String[] args) {
        //变量在申明前必须指明变量的数据类型
        String name = "亦良";
        System.out.println(name);
        int age = 30;
        System.out.println(age);
        //变量的应用
        System.out.println(name + "今年" + age + "岁了");  //这里使用了一个字符串拼接


    }
}